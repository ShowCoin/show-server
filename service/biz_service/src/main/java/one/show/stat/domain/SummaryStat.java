public class SummaryStat {

	private int id;
	private int pv;
	private int play;
	private int videos;
	private int share;
	private int login;
	private int register;
	private double gift;
	private double recharge;
	private int time;
	private int liveMax;
	
	

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	

	public int getPv() {
		return pv;
	}

	public void setPv(int pv) {
		this.pv = pv;
	}

	public int getPlay() {
		return play;
	}

	public void setPlay(int play) {
		this.play = play;
	}

	public int getVideos() {
		return videos;
	}

	public void setVideos(int videos) {
		this.videos = videos;
	}

	public int getShare() {
		return share;
	}

	public void setShare(int share) {
		this.share = share;
	}

	public int getLogin() {
		return login;
	}

	public void setLogin(int login) {
		this.login = login;
	}

	public int getRegister() {
		return register;
	}

	public void setRegister(int register) {
		this.register = register;
	}

	
	public double getGift() {
		return gift;
	}

	public void setGift(double gift) {
		this.gift = gift;
	}

	public double getRecharge() {
		return recharge;
	}

	public void setRecharge(double recharge) {
		this.recharge = recharge;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

	public int getLiveMax() {
		return liveMax;
	}

	public void setLiveMax(int liveMax) {
		this.liveMax = liveMax;
	}
	
	

}
